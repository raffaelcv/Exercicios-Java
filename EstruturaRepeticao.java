
public class EstruturaRepeticao {
	 
	public static void main(String[] args) {
		
		//Utilizando WHILE
		
		int contador = 0;
		
		
		while (contador <= 10) {
			System.out.println("repeticão WHILE " + contador + " de 10");
			contador ++;
		}
			
		System.out.println("\n-----------------------------------");
		
		//Utilizando o FOR
		
		for (int i =0; i <=10; i++) {
			System.out.println("Repetição FOR " + i + " de 10");
		}
		
		
	}
	
}
